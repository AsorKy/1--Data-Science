list_1 = [1,4,23,54,87,98,35,3]

def Max (List):
    largest_so_far = List[0]
    for num in List:
        if num > largest_so_far:
            largest_so_far = num
        else:
            largest_so_far = largest_so_far
    return largest_so_far

Max_num = Max(list_1)

